import turtle;
def draw_sqr() :
    window=turtle.Screen();
    window.bgcolor("blue");
    b= turtle.Turtle()
    b.shape("turtle");
    b.color("yellow")
    b.speed(4)
    b.forward(100)
    b.right(90)
    b.forward(100)
    b.right(90)
    b.forward(100)
    b.right(90)
    b.forward(100)
    b.right(90)
    window.exitonclick()
draw_sqr()
